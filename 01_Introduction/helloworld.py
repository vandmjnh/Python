print(4+4)   # Addition
print(16-8)  # Subtraction
print(2*4)   # Multiplication
print(24/3)  # Division
print(2**3)  # Exponent
print(17%8)  # Modulus
print(19//2) # Floor Division

# Check the type of the result
print(type(8))                        # Integer
print(type(8.0))                      # Float
print(type(2 + 2j))                   # Complex
print(type("Eight"))                  # String
print(type([8, 8, 8, 8, 8]))          # List
print(type({"Eight": 8, "Eight": 8})) # Dictionaries
print(type({8, "eight"}))             # Set
print(type(("Eight",)))               # Tuple